-- Recalculate XP for a user (callable by the user themselves or server)
create or replace function fn_recalc_xp(uid uuid)
returns void as $$
declare
    total int;
    new_level int;
begin
    -- Only allow recalc for self or service role
    if auth.uid() is not null and auth.uid() != uid then
        raise exception 'Not authorized';
    end if;
    select coalesce(sum(xp_delta), 0) into total from xp_events where user_id = uid;
    new_level = greatest(1, (total / 500) + 1);
    update profiles set xp_total = total, level_int = new_level where id = uid;
end;
$$ language plpgsql security definer;

-- Server-side function to create notifications (service role only)
create or replace function fn_create_notification(
    p_user_id uuid,
    p_type text,
    p_title text,
    p_message text default null,
    p_data jsonb default null
)
returns bigint as $$
declare
    new_id bigint;
begin
    insert into notifications (user_id, type, title, message, data)
    values (p_user_id, p_type, p_title, p_message, p_data)
    returning id into new_id;
    return new_id;
end;
$$ language plpgsql security definer;

