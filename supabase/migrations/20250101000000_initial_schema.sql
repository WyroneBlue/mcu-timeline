-- Lorely — Multi-Franchise Watch Tracker
-- Complete schema with multi-franchise, canon/retcon, spoilers, tickets, gamification

-- ============================================================
-- FRANCHISES
-- ============================================================

create table if not exists franchises (
    id bigint generated by default as identity primary key,
    code text unique not null,
    name text not null,
    slug text unique not null,
    description text,
    color_primary text,
    color_accent text,
    sort_order int default 0
);

alter table franchises enable row level security;
create policy "franchises public read" on franchises for select using (true);

-- ============================================================
-- TITLES
-- ============================================================

create table if not exists titles (
    id bigint generated by default as identity primary key,
    franchise_id bigint not null references franchises(id) on delete cascade,
    tmdb_id int,
    type text check (type in ('movie', 'series', 'special', 'short')) not null,
    title text not null,
    slug text unique not null,
    release_date date,
    release_status text check (release_status in ('released', 'upcoming', 'announced')) not null default 'released',
    chronology_index numeric,
    story_year text,
    story_order numeric,
    phase text,
    saga text,
    poster_url text,
    backdrop_url text,
    overview text,
    runtime_minutes int,
    streaming_url text,
    streaming_platform text,
    trailer_url text,
    -- Canon system
    canon_level text check (canon_level in ('core', 'extended', 'adjacent', 'standalone')) not null default 'core',
    mcu_relevance_score int check (mcu_relevance_score between 1 and 10) default 5,
    retcon_notes text,
    retconned_by bigint[],
    retcon_date date
);

create index idx_titles_franchise on titles(franchise_id);
create index idx_titles_chronology on titles(franchise_id, chronology_index);
create index idx_titles_story_order on titles(franchise_id, story_order);
create index idx_titles_release_status on titles(release_status);
